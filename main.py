first_user_input = int(input("Enter first: "))

second_user_input = int(input("Enter second number: "))


if first_user_input < second_user_input:
    print(f"{first_user_input} is less than {second_user_input}")

elif first_user_input > second_user_input:
    print(f"{first_user_input} is greater than {second_user_input}")

elif first_user_input == second_user_input:
    print(f"{first_user_input} is equal to {second_user_input}")


